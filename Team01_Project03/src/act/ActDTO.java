package act;

public class ActDTO {
	// 멤버 변수 선언
    private String actNumber; 	// 숙소 번호
    private String actName; 	// 숙소명
    private String actInfo; 	// 숙소 정보
    private String actAddress;	// 숙소 주소
    private String actPhone;	// 숙소 전화번호
    private int actRoom;		// 숙소 총 객실 수
    private String actId;		// 숙소 작성자 id
    private int actPrice;		// 숙소 가격
    private int actLeftRoom;	// 숙소 남은객실 수
    private String actBookMark;	// 숙소 관심여부
    private String actDiv;		// 숙소 종류
    
	// getter setter ---------------------------------------------
	public String getActNumber() {
		return actNumber;
	}
	public void setActNumber(String actNumber) {
		this.actNumber = actNumber;
	}
	public String getActName() {
		return actName;
	}
	public void setActName(String actName) {
		this.actName = actName;
	}
	public String getActInfo() {
		return actInfo;
	}
	public void setActInfo(String actInfo) {
		this.actInfo = actInfo;
	}
	public String getActAddress() {
		return actAddress;
	}
	public void setActAddress(String actAddress) {
		this.actAddress = actAddress;
	}
	public String getActPhone() {
		return actPhone;
	}
	public void setActPhone(String actPhone) {
		this.actPhone = actPhone;
	}
	public int getActRoom() {
		return actRoom;
	}
	public void setActRoom(int actRoom) {
		this.actRoom = actRoom;
	}
	public String getActId() {
		return actId;
	}
	public void setActId(String actId) {
		this.actId = actId;
	}
	public int getActPrice() {
		return actPrice;
	}
	public void setActPrice(int actPrice) {
		this.actPrice = actPrice;
	}

	public int getActLeftRoom() {
		return actLeftRoom;
	}
	public void setActLeftRoom(int actLeftRoom) {
		this.actLeftRoom = actLeftRoom;
	}
	public String getActBookMark() {
		return actBookMark;
	}
	public void setActBookMark(String actBookMark) {
		this.actBookMark = actBookMark;
	}
	public String getActDiv() {
		return actDiv;
	}
	public void setActDiv(String actDiv) {
		this.actDiv = actDiv;
	}
	// getter setter ---------------------------------------------
}

   

